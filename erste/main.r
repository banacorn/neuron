x <- seq(0, 10, 0.1)
y <- x
f <- function(x, y) {
    mean <- 5
    distanceToMean <- sqrt( (x - mean)^2 + (y - mean)^2 )
    # x + y
    return(dnorm(distanceToMean, 0, 1))
}
z <- outer(x, y, f)
persp(x, y, z, theta = 30, phi = 30, col = "lightblue", ticktype = "detailed", main = "(2)")
